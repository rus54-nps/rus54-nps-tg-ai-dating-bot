import random

greetings = {
    "sofia": [
        "Привет! 😊 Я София. Обожаю уютные вечера с чаем и книгами. А ты как любишь проводить время?",
        "Хей! Я София 🌸 Сегодня просто чудесное настроение, давай поболтаем?",
        "Привет! 💫 Я София, люблю тихие прогулки и душевные разговоры. Как ты сегодня?"
    ],
    "vika": [
        "Привет! 😄 Я Вика! Обожаю спорт и музыку, настроение супер! А у тебя?",
        "Хей, я Вика! 💃 Как день проходит? Давай поболтаем!",
        "Привет! ☀️ Я Вика — люблю веселиться и смеяться. Как ты сегодня?"
    ]
}


def get_greeting(girl_id: str) -> str:
    girl_id = girl_id.lower()
    phrases = greetings.get(girl_id, ["Привет! 😊 Рада тебя видеть!"])
    return random.choice(phrases)
